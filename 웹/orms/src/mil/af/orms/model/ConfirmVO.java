package mil.af.orms.model;

public class ConfirmVO {
	int seq;
	String sn;
	String sosokcode;
	String confirm_date;
	String opinion;
	String result_fix;
	String pilot_rankname;
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public String getSosokcode() {
		return sosokcode;
	}
	public void setSosokcode(String sosokcode) {
		this.sosokcode = sosokcode;
	}
	public String getConfirm_date() {
		return confirm_date;
	}
	public void setConfirm_date(String confirm_date) {
		this.confirm_date = confirm_date;
	}
	public String getOpinion() {
		return opinion;
	}
	public void setOpinion(String opinion) {
		this.opinion = opinion;
	}
	public String getResult_fix() {
		return result_fix;
	}
	public void setResult_fix(String result_fix) {
		this.result_fix = result_fix;
	}
	public String getPilot_rankname() {
		return pilot_rankname;
	}
	public void setPilot_rankname(String pilot_rankname) {
		this.pilot_rankname = pilot_rankname;
	}
	
}
