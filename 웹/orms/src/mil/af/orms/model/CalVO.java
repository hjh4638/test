package mil.af.orms.model;

public class CalVO {
	String seq;
	String question_id;
	String score;
	String connection_yn;
	String c_question_id;
	String c_score;
	String end_score;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getQuestion_id() {
		return question_id;
	}
	public void setQuestion_id(String question_id) {
		this.question_id = question_id;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getConnection_yn() {
		return connection_yn;
	}
	public void setConnection_yn(String connection_yn) {
		this.connection_yn = connection_yn;
	}
	public String getC_question_id() {
		return c_question_id;
	}
	public void setC_question_id(String c_question_id) {
		this.c_question_id = c_question_id;
	}
	public String getC_score() {
		return c_score;
	}
	public void setC_score(String c_score) {
		this.c_score = c_score;
	}
	public String getEnd_score() {
		return end_score;
	}
	public void setEnd_score(String end_score) {
		this.end_score = end_score;
	}
	
}	
